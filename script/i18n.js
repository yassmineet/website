function changeLanguage(lang) {
    if (!translations[lang]) return;

    // Save preference
    localStorage.setItem('preferredLanguage', lang);

    // Update HTML dir and lang
    document.documentElement.lang = lang;
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        document.body.classList.add('rtl-layout');
    } else {
        document.documentElement.dir = 'ltr';
        document.body.classList.remove('rtl-layout');
    }

    // Update text content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Replace innerHTML to support embedded HTML like <i> or <br>
            element.innerHTML = translations[lang][key];
        }
    });

    // Update active state of language switcher buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Handle arrows inside buttons that aren't replaced entirely by innerHTML
    // For service buttons that have both text and an icon that points right/left:
    const srvBtns = document.querySelectorAll('.service-link i');
    srvBtns.forEach(icon => {
        if (lang === 'ar') {
            icon.classList.remove('fa-arrow-right');
            icon.classList.add('fa-arrow-left');
        } else {
            icon.classList.remove('fa-arrow-left');
            icon.classList.add('fa-arrow-right');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    // Default to 'fr' if no preference is explicitly set to something else
    const savedLang = localStorage.getItem('preferredLanguage') || 'fr';
    changeLanguage(savedLang);

    // Attach event listeners to language switcher
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = btn.dataset.lang;
            changeLanguage(lang);
        });
    });
});
